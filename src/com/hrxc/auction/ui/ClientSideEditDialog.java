    private void submitBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtActionPerformed
        //进行输入项验证
        if (UITools.vilidateText(fd_clientNo, msgLabel, true, false, false)
                && UITools.vilidateText(fd_clientName, msgLabel, true, false, false)) {

            //如果数据对象为空则新建
            if (dto == null) {
                dto = new ClientSide();
            }

            //拼装数据对象属性
            dto.setClientNo(fd_clientNo.getText().trim());
            dto.setClientName(fd_clientName.getText().trim());

            if (JOptionPane.showConfirmDialog(this.getRootPane(), "请确认您是否要保存数据？") == JOptionPane.YES_OPTION) {
                ClientSideAction.saveOrUpdateObject(dto);
                JOptionPane.showMessageDialog(this, "保存成功！");
                this.dispose();
                ClientSidePanel panel = (ClientSidePanel) this.listDataPanel;
                panel.refreshTableDatas(null, null);
            }

        }
    }//GEN-LAST:event_submitBtActionPerformed
